function min(num1, num2) {
  return num1 > num2 ? num2: num1;
}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(num) {
  if (num % 2 === 0){
    return true;
  } else if (num % 2 === 1){
    return false;
  } else if (num < 0){
    return false;
  }
}

