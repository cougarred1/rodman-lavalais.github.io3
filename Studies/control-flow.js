/**
 * CONTROL FLOW
 * 
 * 0: Control flow refers to how body of code is executing based on conditions
 * provided. It includes conditional statements.
 * 
 * 1: If statements execute if a condition is true
 * 
 * 2 and 3. else if and else provide alternate code if the if statement is false
 * 
 * 4. Switch statements allow us to select one of many blocks to be executed
 * 
 * 
 */

//if

if (condition) {
    // Code that will run if condition is true
}

// else if and else

if (condition1) {
    // Code that will run if condition1 is true
} else if (condition2) {
    // Code that will run if condition2 is true
} else {
    // Code that will run if no condition is true
}

//switch
switch (expression) {
    case val1:
        // Code that will run if expression equals val1
        break;
    case val2:
        // Code that will run if expression equals val2
        break;
    default:
        // Code that will run if expression doesn't match any case
}

